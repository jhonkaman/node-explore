// Boolean Variables

// Run: node 04-booleans/01-boolean-variables.js

// Learn how to use true and false boolean values

let isRaining = true;
let isSunny = false;
let isWeekend = true;

console.log(isRaining);
console.log(isSunny);
console.log(isWeekend);

// Booleans with comparisons
let age = 20;
let isAdult = age >= 18;
console.log(isAdult);

let score = 85;
let isPassing = score >= 60;
console.log(isPassing);
