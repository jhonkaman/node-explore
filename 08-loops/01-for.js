// For Loop

// Run: node 08-loops/01-for.js

// Learn how to use for loops to repeat code

for (let i = 0; i < 5; i++) {
  console.log(i);
}

console.log("---");

// Loop through an array
let fruits = ["apple", "banana", "orange"];

for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}
