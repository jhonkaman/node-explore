// Else Statement

// Run: node 07-conditionals/02-else.js

// Learn how to use else statements for alternative code paths

let age = 15;

if (age >= 18) {
  console.log("You are an adult");
} else {
  console.log("You are a minor");
}

let score = 55;

if (score >= 60) {
  console.log("You passed");
} else {
  console.log("You failed");
}
