// If Statement

// Run: node 07-conditionals/01-if.js

// Learn how to use if statements to execute code conditionally

let age = 18;

if (age >= 18) {
  console.log("You are an adult");
}

let score = 85;

if (score >= 80) {
  console.log("Great score!");
}

if (score < 60) {
  console.log("You failed");
}
