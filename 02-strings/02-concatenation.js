// Concatenation

// Run: node 02-strings/02-concatenation.js

// Combine multiple strings together using concatenation

let firstName = "John";
let lastName = "Doe";

// Using the + operator
let fullName = firstName + " " + lastName;
console.log(fullName);

// Using template literals
let greeting = `Hello, ${firstName}!`;
console.log(greeting);

let sentence = `${firstName} ${lastName} lives in Boston.`;
console.log(sentence);
