// Arithmetic Operators

// Run: node 06-operators/01-arithmetic-operators.js

// Learn how to use arithmetic operators: +, -, *, /, %, **

let a = 10;
let b = 3;

// Addition
console.log(a + b);

// Subtraction
console.log(a - b);

// Multiplication
console.log(a * b);

// Division
console.log(a / b);

// Modulo (remainder)
console.log(a % b);

// Exponentiation
console.log(a ** b);
