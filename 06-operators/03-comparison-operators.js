// Comparison Operators

// Run: node 06-operators/03-comparison-operators.js

// Learn how to use comparison operators: ==, ===, !=, !==, <, >, <=, >=

let a = 10;
let b = 20;

// Equal to
console.log(a == b);
console.log(a == 10);

// Strict equal to
console.log(a === 10);
console.log("10" === 10);

// Not equal to
console.log(a != b);
console.log(a != 10);

// Less than
console.log(a < b);

// Greater than
console.log(a > b);

// Less than or equal to
console.log(a <= b);
console.log(a <= 10);

// Greater than or equal to
console.log(a >= b);
console.log(a >= 10);
