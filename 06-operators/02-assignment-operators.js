// Assignment Operators

// Run: node 06-operators/02-assignment-operators.js

// Learn how to use assignment operators: =, +=, -=, *=, /=

let x = 10;
console.log(x);

// Assignment
x = 20;
console.log(x);

// Addition assignment
x += 5;
console.log(x);

// Subtraction assignment
x -= 3;
console.log(x);

// Multiplication assignment
x *= 2;
console.log(x);

// Division assignment
x /= 4;
console.log(x);
